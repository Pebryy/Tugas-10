# Heapify saat insert
def heapifyTreeInsert(rootNode, index, heapType):

    # Menghitung parent node
    parentIndex = int(index / 2)

    # Jika sudah sampai root
    if index <= 1:
        return

    # Untuk Min Heap
    if heapType == "Min":

        if rootNode.customList[index] < rootNode.customList[parentIndex]:

            temp = rootNode.customList[index]
            rootNode.customList[index] = rootNode.customList[parentIndex]
            rootNode.customList[parentIndex] = temp

        heapifyTreeInsert(rootNode, parentIndex, heapType)

    # Untuk Max Heap
    elif heapType == "Max":

        if rootNode.customList[index] > rootNode.customList[parentIndex]:

            temp = rootNode.customList[index]
            rootNode.customList[index] = rootNode.customList[parentIndex]
            rootNode.customList[parentIndex] = temp

        heapifyTreeInsert(rootNode, parentIndex, heapType)


# Menambahkan node ke heap
def inserNode(rootNode, nodeValue, heapType):

    # Mengecek apakah heap penuh
    if rootNode.heapSize + 1 == rootNode.maxSize:
        return "The Binary Heap is Full"

    # Menambahkan node di akhir heap
    rootNode.customList[rootNode.heapSize + 1] = nodeValue

    rootNode.heapSize += 1

    # Menyeimbangkan heap
    heapifyTreeInsert(
        rootNode,
        rootNode.heapSize,
        heapType
    )

    return "The value has been successfully inserted"


# Heapify saat extract/delete root
def heapifyTreeExtract(rootNode, index, heapType):

    leftIndex = index * 2
    rightIndex = index * 2 + 1

    swapChild = 0

    # Tidak memiliki child
    if rootNode.heapSize < leftIndex:
        return

    # Hanya memiliki child kiri
    elif rootNode.heapSize == leftIndex:

        if heapType == "Min":

            if rootNode.customList[index] > rootNode.customList[leftIndex]:

                temp = rootNode.customList[index]
                rootNode.customList[index] = rootNode.customList[leftIndex]
                rootNode.customList[leftIndex] = temp

            return

        else:

            if rootNode.customList[index] < rootNode.customList[leftIndex]:

                temp = rootNode.customList[index]
                rootNode.customList[index] = rootNode.customList[leftIndex]
                rootNode.customList[leftIndex] = temp

            return

    else:

        # Min Heap
        if heapType == "Min":

            if rootNode.customList[leftIndex] < rootNode.customList[rightIndex]:
                swapChild = leftIndex
            else:
                swapChild = rightIndex

            if rootNode.customList[index] > rootNode.customList[swapChild]:

                temp = rootNode.customList[index]
                rootNode.customList[index] = rootNode.customList[swapChild]
                rootNode.customList[swapChild] = temp

        # Max Heap
        else:

            if rootNode.customList[leftIndex] > rootNode.customList[rightIndex]:
                swapChild = leftIndex
            else:
                swapChild = rightIndex

            if rootNode.customList[index] < rootNode.customList[swapChild]:

                temp = rootNode.customList[index]
                rootNode.customList[index] = rootNode.customList[swapChild]
                rootNode.customList[swapChild] = temp

    # Rekursif sampai heap kembali valid
    heapifyTreeExtract(rootNode, swapChild, heapType)


