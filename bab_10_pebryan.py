# Mengambil tinggi node
def getHeight(rootNode):

    if not rootNode:
        return 0

    return rootNode.height


# Rotasi kanan (Right Rotation)
def rightRotate(disbalanceNode):

    # Node baru menjadi root
    newRoot = disbalanceNode.leftChild

    # Mengatur hubungan node
    disbalanceNode.leftChild = disbalanceNode.leftChild.rightChild
    newRoot.rightChild = disbalanceNode

    # Update tinggi node
    disbalanceNode.height = (
        1 + max(
            getHeight(disbalanceNode.leftChild),
            getHeight(disbalanceNode.rightChild)
        )
    )

    newRoot.height = (
        1 + max(
            getHeight(newRoot.leftChild),
            getHeight(newRoot.rightChild)
        )
    )

    return newRoot


# Rotasi kiri (Left Rotation)
def leftRotate(disbalanceNode):

    # Node baru menjadi root
    newRoot = disbalanceNode.rightChild

    # Mengatur hubungan node
    disbalanceNode.rightChild = disbalanceNode.rightChild.leftChild
    newRoot.leftChild = disbalanceNode

    # Update tinggi node
    disbalanceNode.height = (
        1 + max(
            getHeight(disbalanceNode.leftChild),
            getHeight(disbalanceNode.rightChild)
        )
    )

    newRoot.height = (
        1 + max(
            getHeight(newRoot.leftChild),
            getHeight(newRoot.rightChild)
        )
    )

    return newRoot


# Menghitung Balance Factor
def getBalance(rootNode):

    if not rootNode:
        return 0

    return (
        getHeight(rootNode.leftChild)
        - getHeight(rootNode.rightChild)
    )


# Menambahkan node ke AVL Tree
def insertNode(rootNode, nodeValue):

    # Membuat node baru jika root kosong
    if not rootNode:
        return AVLNode(nodeValue)

    elif nodeValue < rootNode.data:
        rootNode.leftChild = insertNode(
            rootNode.leftChild,
            nodeValue
        )

    else:
        rootNode.rightChild = insertNode(
            rootNode.rightChild,
            nodeValue
        )

    # Update tinggi node
    rootNode.height = (
        1 + max(
            getHeight(rootNode.leftChild),
            getHeight(rootNode.rightChild)
        )
    )

    # Hitung balance factor
    balance = getBalance(rootNode)

    # Left Left Case
    if balance > 1 and nodeValue < rootNode.leftChild.data:
        return rightRotate(rootNode)

    # Left Right Case
    if balance > 1 and nodeValue > rootNode.leftChild.data:
        rootNode.leftChild = leftRotate(rootNode.leftChild)
        return rightRotate(rootNode)

    # Right Right Case
    if balance < -1 and nodeValue > rootNode.rightChild.data:
        return leftRotate(rootNode)

    # Right Left Case
    if balance < -1 and nodeValue < rootNode.rightChild.data:
        rootNode.rightChild = rightRotate(rootNode.rightChild)
        return leftRotate(rootNode)

    return rootNode


# Mengambil node dengan nilai terkecil
def getMinValueNode(rootNode):

    if rootNode is None or rootNode.leftChild is None:
        return rootNode

    return getMinValueNode(rootNode.leftChild)


# Menghapus node dari AVL Tree
def deleteNode(rootNode, nodeValue):

    if not rootNode:
        return rootNode

    elif nodeValue < rootNode.data:
        rootNode.leftChild = deleteNode(
            rootNode.leftChild,
            nodeValue
        )

    elif nodeValue > rootNode.data:
        rootNode.rightChild = deleteNode(
            rootNode.rightChild,
            nodeValue
        )

    else:

        # Tidak memiliki child kiri
        if rootNode.leftChild is None:
            temp = rootNode.rightChild
            rootNode = None
            return temp

        # Tidak memiliki child kanan
        elif rootNode.rightChild is None:
            temp = rootNode.leftChild
            rootNode = None
            return temp

        # Memiliki dua child
        temp = getMinValueNode(rootNode.rightChild)

        rootNode.data = temp.data

        rootNode.rightChild = deleteNode(
            rootNode.rightChild,
            temp.data
        )

    # Hitung balance factor
    balance = getBalance(rootNode)

    # Left Left Case
    if balance > 1 and getBalance(rootNode.leftChild) >= 0:
        return rightRotate(rootNode)

    # Right Right Case
    if balance < -1 and getBalance(rootNode.rightChild) <= 0:
        return leftRotate(rootNode)

    # Left Right Case
    if balance > 1 and getBalance(rootNode.leftChild) < 0:
        rootNode.leftChild = leftRotate(rootNode.leftChild)
        return rightRotate(rootNode)

    # Right Left Case
    if balance < -1 and getBalance(rootNode.rightChild) > 0:
        rootNode.rightChild = rightRotate(rootNode.rightChild)
        return leftRotate(rootNode)

    return rootNode


