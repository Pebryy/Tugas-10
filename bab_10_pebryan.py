# Membuat class TrieNode
# Setiap node menyimpan karakter dan child-node
class TrieNode:

    # Constructor
    def __init__(self):

        # Menyimpan child dalam bentuk dictionary
        self.children = {}

        # Menandakan akhir sebuah kata
        self.endOfString = False


# Membuat class Trie
class Trie:

    # Constructor
    def __init__(self):

        # Membuat root node
        self.root = TrieNode()

    # Menambahkan kata ke Trie
    def insertString(self, word):

        # Mulai dari root
        current = self.root

        # Menelusuri setiap karakter
        for i in word:

            ch = i

            # Mencari karakter dalam children
            node = current.children.get(ch)

            # Jika karakter belum ada
            if node == None:

                # Membuat node baru
                node = TrieNode()

                # Menambahkan ke dictionary children
                current.children.update({ch: node})

            # Pindah ke node berikutnya
            current = node

        # Menandai akhir kata
        current.endOfString = True

        print("Successfully inserted")

    # Mencari kata dalam Trie
    def searchString(self, word):

        # Mulai dari root
        currentNode = self.root

        # Menelusuri setiap karakter
        for i in word:

            node = currentNode.children.get(i)

            # Jika karakter tidak ditemukan
            if node == None:
                return False

            currentNode = node

        # Jika benar-benar akhir kata
        if currentNode.endOfString == True:
            return True

        else:
            return False


# Menghapus kata dari Trie
def deleteString(root, word, index):

    # Mengambil karakter saat ini
    ch = word[index]

    # Mengambil node karakter
    currentNode = root.children.get(ch)

    canThisNodeBeDeleted = False

    # Jika node memiliki lebih dari satu child
    if len(currentNode.children) > 1:

        deleteString(currentNode, word, index + 1)

        return False

    # Jika berada di karakter terakhir
    if index == len(word) - 1:

        # Jika masih memiliki child
        if len(currentNode.children) >= 1:

            # Hanya hilangkan penanda akhir kata
            currentNode.endOfString = False

            return False

        else:
            # Hapus node dari parent
            root.children.pop(ch)

            return True

    # Jika node juga merupakan akhir kata lain
    if currentNode.endOfString == True:

        deleteString(currentNode, word, index + 1)

        return False

    # Rekursif ke karakter berikutnya
    canThisNodeBeDeleted = deleteString(
        currentNode,
        word,
        index + 1
    )

    # Jika child bisa dihapus
    if canThisNodeBeDeleted == True:

        root.children.pop(ch)

        return True

    else:
        return False



